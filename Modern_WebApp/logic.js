/**
 * Calculates the new salary step, taking ceiling limits into account.
 * @param {number} posType - Column index from 1-13
 * @param {number} salary - Current salary matching exactly in T
 * @param {number} evalVal - Step increment (0, 0.5, 1, 1.5, 2)
 * @returns {Object} result payload
 */
function calculateSalary(posType, salary, evalVal) {
    const col = posType;
    const ci = T.findIndex(row => row[col] === salary);

    if (ci === -1) {
        throw new Error(`ไม่พบเงินเดือน ${salary.toLocaleString('th-TH')} บาท ในระดับหมวด "${PNAME[posType]}" โปรดตรวจสอบจากสลิป`);
    }

    const cs = T[ci][0]; // Current step
    const tgt = cs + evalVal; // Target step computationally

    let ni = T.findIndex(row => row[0] === tgt && row[col] != null);
    let ceil = false;

    if (ni === -1) {
        // Cannot directly find the target step. Might be ceiling. 
        // Go upwards (lower index) finding the most max acceptable row
        for (let i = ci - 1; i >= 0; i--) {
            if (T[i][col] != null) {
                ni = i;
                break;
            }
        }
        // If absolutely no increment possible, cap it
        if (ni === -1) ni = ci;
        ceil = true;
    }

    const csalv = T[ci][col];
    const nsalv = T[ni][col];
    const diff = nsalv - csalv;
    const ns = T[ni][0];

    return {
        pt: posType,
        sal: salary,
        cs: cs,
        ns: ns,
        csalv: csalv,
        nsalv: nsalv,
        diff: diff,
        ev: evalVal,
        ceil: ceil,
        col: col
    };
}
